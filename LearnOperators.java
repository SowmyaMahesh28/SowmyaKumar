package week1.day2;

public class LearnOperators {
public static void main(String[] args) {
	int a =9;
	int b= 6;
	//arthematic
	System.out.println(a+b);
	System.out.println(a-b);
	System.out.println(a*b);
	System.out.println(a/b);
	System.out.println(a%b);
	//Comparison 
	System.out.println(a>b);
	System.out.println(a<b);
	System.out.println(a>=b);
	System.out.println(a<=b);
	System.out.println(a==b);
	System.out.println(a!=b);
	
	//logicoperators
	
	System.out.println(a>b&&a<b);
	System.out.println(a>b||a<b);
	
	
}
}
